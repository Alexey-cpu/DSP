#include "cplx_num.hpp"

//-----------------------------------------------------------------------------------------------------------------------------------
// конструктор по умолчанию:
//-----------------------------------------------------------------------------------------------------------------------------------
cplx_num::cplx_num()
{
	m_re  = 0;
	m_im  = 0;
}
//-----------------------------------------------------------------------------------------------------------------------------------
// перегрузки конструктора:

// инициализация только действительной части:
cplx_num::cplx_num(cplx_type re) 
{
	m_re = re;
	m_im = 0.0;
}

// инициализация действительной и мнимой части:
cplx_num::cplx_num(cplx_type re , cplx_type im)
{
	m_re = re;
	m_im = im;
}

// копирование значения другого комплексного числа:
cplx_num::cplx_num(const cplx_num &cplx)
{
	m_re = cplx.m_re;
	m_im = cplx.m_im;
}
//-----------------------------------------------------------------------------------------------------------------------------------
// деструктор:
cplx_num::~cplx_num() {};
