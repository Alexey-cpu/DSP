#include "integrators.hpp"

//------------------------------------------------------------------------------------------------
// конструктор по умолчанию:
//------------------------------------------------------------------------------------------------
integrators::integrators()
{
	m_Fn = 50;
	m_Fs = 4000;
	m_Ts = 1 / m_Fs;
		
	m_out         = 0;
	m_prev_input  = 0;
	m_prev_output = 0;
}
//------------------------------------------------------------------------------------------------
// деструктор:
//------------------------------------------------------------------------------------------------
integrators::~integrators(){}
//------------------------------------------------------------------------------------------------
// функция инициализации:
//------------------------------------------------------------------------------------------------
int integrators::int_Init( double Fs , double Fn )
{
	m_Fn = Fn;
	m_Fs = Fs;
	m_Ts = 1 / m_Fs;
	
	m_out         = 0;
	m_prev_input  = 0;
	m_prev_output = 0;

    return 0;
}
